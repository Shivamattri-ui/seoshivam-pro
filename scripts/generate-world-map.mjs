/**
 * Natural Earth 110m land + geodesic coverage arcs.
 * Run: node scripts/generate-world-map.mjs
 */
import { writeFileSync } from 'node:fs';
import { geoPath, geoEquirectangular, geoInterpolate } from 'd3-geo';
import { feature } from 'topojson-client';

const WIDTH = 1000;
const HEIGHT = 500;

const projection = geoEquirectangular().fitExtent(
  [[12, 10], [WIDTH - 12, HEIGHT - 10]],
  { type: 'Sphere' }
);

const pathGen = geoPath(projection);

const landRes = await fetch('https://cdn.jsdelivr.net/npm/world-atlas@2/land-110m.json');
const landTopo = await landRes.json();
const land = feature(landTopo, landTopo.objects.land);
const landPaths = land.features.map((f) => pathGen(f)).filter(Boolean);

const markets = [
  { id: 'usa', label: 'USA', lon: -98.35, lat: 39.5, tip: 'AEO & SEO Expert in the United States · Shivam Attri' },
  { id: 'canada', label: 'Canada', lon: -106.35, lat: 56.13, tip: 'AEO & SEO Expert in Canada · Shivam Attri' },
  { id: 'uk', label: 'UK', lon: -1.5, lat: 53.5, tip: 'AEO & SEO Expert in the United Kingdom · Shivam Attri' },
  { id: 'ireland', label: 'Ireland', lon: -7.69, lat: 53.14, tip: 'AEO & SEO Expert in Ireland · Shivam Attri' },
  { id: 'germany', label: 'Germany', lon: 10.45, lat: 51.17, tip: 'AEO & SEO Expert in Germany · Shivam Attri' },
  { id: 'eu', label: 'EU', lon: 4.35, lat: 50.85, tip: 'AEO & SEO Expert in the European Union · Shivam Attri' },
  { id: 'india', label: 'India', lon: 77.03, lat: 28.46, tip: 'AEO & SEO Expert in India · Shivam Attri · Gurugram HQ', hub: true },
  { id: 'singapore', label: 'Singapore', lon: 103.82, lat: 1.35, tip: 'AEO & SEO Expert in Singapore · Shivam Attri' },
  { id: 'australia', label: 'Australia', lon: 133.78, lat: -25.27, tip: 'AEO & SEO Expert in Australia · Shivam Attri' },
];

function project(lon, lat) {
  const p = projection([lon, lat]);
  return { x: Math.round(p[0] * 100) / 100, y: Math.round(p[1] * 100) / 100 };
}

function geodesicArc(lon1, lat1, lon2, lat2, steps = 64) {
  const interp = geoInterpolate([lon1, lat1], [lon2, lat2]);
  const coordinates = [];
  for (let i = 0; i <= steps; i++) coordinates.push(interp(i / steps));
  const d = pathGen({ type: 'LineString', coordinates });
  return d || '';
}

const projected = markets.map((m) => {
  const { x, y } = project(m.lon, m.lat);
  return { ...m, x, y };
});

const hub = projected.find((m) => m.hub);
const spokes = projected.filter((m) => !m.hub);
const arcs = spokes.map((m) => ({
  id: m.id,
  d: geodesicArc(hub.lon, hub.lat, m.lon, m.lat),
}));

const out = `/** Auto-generated by scripts/generate-world-map.mjs — do not edit by hand. */
export const MAP_WIDTH = ${WIDTH};
export const MAP_HEIGHT = ${HEIGHT};

export const WORLD_LAND_PATHS: string[] = ${JSON.stringify(landPaths)};

export type CoverageMarket = {
  id: string;
  label: string;
  tip: string;
  x: number;
  y: number;
  lon: number;
  lat: number;
  hub?: boolean;
};

export const COVERAGE_MARKETS: CoverageMarket[] = ${JSON.stringify(projected, null, 2)};

export const COVERAGE_ARCS: { id: string; d: string }[] = ${JSON.stringify(arcs, null, 2)};
`;

writeFileSync('src/lib/world-map-data.ts', out, 'utf8');
console.log(`Wrote ${landPaths.length} land path(s), ${projected.length} markets, ${arcs.length} arcs.`);
